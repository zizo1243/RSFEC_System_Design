TEST_F(test_RSFEDdecoder, NoErrorsDetected) {
    EncodedMessage encodedMessage{};
    encodedMessage.SetField(10);
    encodedMessage.SetSize(544);
    encodedMessage.SetcodewordVector(em_no_errorvec);
    EXPECT_NO_THROW({
        RSFEDdecoder decoder(encodedMessage, 10, 544, 514);
        std::vector<std::string> locations = decoder.Geterrorlocations();
        ASSERT_EQ(locations.size(), 1);
        EXPECT_EQ(locations[0], " no symbols");
    });
}
TEST_F(test_RSFEDdecoder, DetectsFewErrorsCorrectly) {
    EncodedMessage encodedMessage{};
    encodedMessage.SetField(10);
    encodedMessage.SetSize(544);
    encodedMessage.SetcodewordVector(em_lt_t_errorvec);
    vector<int>locations_expected = {201,435,496,497,498,499,500,501,502,503,504,505,506,507,508};
    EXPECT_NO_THROW({
        RSFEDdecoder decoder(encodedMessage, 10, 544, 514);
        std::vector<std::string> locations = decoder.Geterrorlocations();

        // We expect error positions (strings like "position x"), not just one summary
        ASSERT_GT(locations.size(), 0);
        EXPECT_EQ(locations.size(),locations_expected.size());
      for (int i=0;i<locations_expected.size();i++){
          std::string s = locations[i];
            int number = -1;
            std::istringstream iss(s);
            std::string word;
            while (iss >> word) {
                try {
                    number = std::stoi(word);
                    break;
                } catch (...) {

                }
            }

            EXPECT_EQ(locations_expected[i],number);
        }

    });
}


